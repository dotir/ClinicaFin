public class citaadjuntos {
    private int idCitaAdjunto;
    private String adjuntos;
    private String detalles;
    private int idCita;

    public int getIdCitaAdjunto() {
        return idCitaAdjunto;
    }

    public void setIdCitaAdjunto(int idCitaAdjunto) {
        this.idCitaAdjunto = idCitaAdjunto;
    }

    public String getAdjuntos() {
        return adjuntos;
    }

    public void setAdjuntos(String adjuntos) {
        this.adjuntos = adjuntos;
    }

    public String getDetalles() {
        return detalles;
    }

    public void setDetalles(String detalles) {
        this.detalles = detalles;
    }

    public int getIdCita() {
        return idCita;
    }

    public void setIdCita(int idCita) {
        this.idCita = idCita;
    }
    
    
    
}
