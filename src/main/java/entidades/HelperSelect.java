package entidades;

/**
 *
 * @author ALESSO
 */
public class HelperSelect {
    
    private int id;
    private String Nombre;

    public HelperSelect(int id, String Nombre) {
        this.id = id;
        this.Nombre = Nombre;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
}
